.MODEL	SMALL
.STACK	100H

.DATA
	MSG_IN	DB	"ENTER A NUMBER: $"
	MSG_RES	DB	13, 10, "RESULT: $"
	MSG_ERR	DB	13, 10, "INVALID INPUT! (NOT A DIGIT OR OVERFLOW)$"

.CODE
MAIN	PROC
	MOV	AX, @DATA
	MOV	DS, AX
	
	MOV	AH, 09H
	LEA	DX, MSG_IN
	INT	21H

	CALL	READ_N

	SUB	AX, 99

	PUSH	AX

	MOV	AH, 09H
	LEA	DX, MSG_RES
	INT	21H
	
	POP	AX

	CALL	PRINT_N

	MOV	AX, 4C00H
	INT	21H
MAIN	ENDP

READ_N	PROC
	PUSH	BX
	PUSH	CX
	PUSH	DX

	XOR	BX, BX
	XOR	CX, CX

READ_LOOP:
	MOV	AH, 01H
	INT	21H

	CMP	AL, 0DH
	JE	DONE_READ

	CMP	AL, '-'
	JNE	CHK_DIGIT

	CMP	BX, 0
	JNE	TRIG_ERR
	MOV	CX, 1
	JMP	READ_LOOP

CHK_DIGIT:
	CMP	AL, '0'
	JB	TRIG_ERR
	CMP	AL, '9'
	JA	TRIG_ERR

	SUB	AL, '0'
	XOR	AH, AH
	PUSH	AX

	MOV	AX, BX
	MOV	BX, 10
	IMUL	BX
	JO	TRIG_ERR_POP

	POP	DX
	ADD	AX, DX
	JO	TRIG_ERR

	MOV	BX, AX
	JMP	READ_LOOP

TRIG_ERR_POP:
	POP	DX

TRIG_ERR:
	MOV	AH, 09H
	LEA	DX, MSG_ERR
	INT	21H

	MOV	AX, 4C01H
	INT	21H

DONE_READ:
	MOV	AX, BX

	CMP	CX, 1
	JNE	FINISH_READ
	NEG	AX

FINISH_READ:
	POP	DX
	POP	CX
	POP	BX
	RET
READ_N	ENDP

PRINT_N	PROC
	PUSH	AX
	PUSH	BX
	PUSH	CX
	PUSH	DX

	MOV	BX, AX

	OR	BX, BX

	JNS	BEGIN_DIV

	MOV	AH, 02H
	MOV	DL, '-'
	INT	21H

	NEG	BX

BEGIN_DIV:
	MOV	AX, BX
	XOR	CX, CX
	MOV	BX, 10

DIV_LOOP:
	XOR	DX, DX
	DIV	BX

	ADD	DL, '0'
	PUSH	DX
	INC	CX

	TEST	AX, AX
	JNZ	DIV_LOOP

PRINT_LOOP:
	POP	DX
	MOV	AH, 02H
	INT	21H
	LOOP	PRINT_LOOP

	POP	DX
	POP	CX
	POP	BX
	POP	AX
PRINT_N	ENDP

END	MAIN
