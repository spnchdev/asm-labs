.MODEL	SMALL
.STACK	100H

.DATA
	MSG_IN		DB "ENTER A NUMBER [-32669; 65535]: $"
	MSG_RES		DB 13, 10, "RESULT: $"
	MSG_ERR_C	DB 13, 10, "ERROR: INVALID CHARACTER!$"
	MSG_ERR_O	DB 13, 10, "ERROR: VALUE OUT OF BOUNDS!$"

	; INPUT BUFFER STRUCTURE
	BUF		DB 8		; "-NNNNN" + ENTER + SAFETY
	ACT_LEN		DB ?		; ACTUAL NUM OF KEYSTROKES READ BY DOS
	CHARS		DB 8 DUP(?)	; ACTUAL CHARS

	; STATE TRACKING
	IS_NEG		DB 0		; 1 IF INPUT WAS NEGATIVE
	IS_LARGE_U	DB 0		; 1 IF INPUT > 32767

.CODE
MAIN	PROC
	MOV	AX, @DATA
	MOV	DS, AX

	MOV	AH, 09H
	LEA	DX, MSG_IN
	INT	21H

	CALL	READ_N		; THE READ NUMBER IS STORED IN AX

	SUB	AX, 99		; CORE MATH OP

	PUSH	AX
	MOV	AH, 09H
	LEA	DX, MSG_RES
	INT	21H
	POP	AX

	CALL	PRINT_N

	MOV	AX, 4C00H
	INT	21H
MAIN	ENDP

READ_N	PROC
	MOV	AH, 0AH
	LEA	DX, BUF
	INT	21H

	; PARSING SETUP
	MOV	CL, ACT_LEN
	XOR	CH, CH
	CMP	CX, 0
	JE	ERR_BOUNDS_LBL	; PREVENT EMPTY INPUT CRASH

	LEA	SI, CHARS
	MOV	IS_NEG, 0
	MOV	IS_LARGE_U, 0
	XOR	DI, DI		; DI WILL ACT AS TEMP 16-BIT ACCUMULATOR

	; CHECK FOR '-' SIGN
	MOV	AL, [SI]
	CMP	AL, '-'
	JNE	PARSE_LOOP

	MOV	IS_NEG, 1
	INC	SI
	DEC	CX
	JZ	ERR_CHAR_LBL	; TRIGGER ERROR IF USER ONLY TYPED '-'

PARSE_LOOP:
	XOR	AX, AX
	LODSB			; LOAD BYTE AT [SI] INTO AL, INC SI

	CMP	AL, '0'
	JB	ERR_CHAR_LBL
	CMP	AL, '9'
	JA	ERR_CHAR_LBL

	SUB	AL, '0'		; ASCII -> NUMBER
	XOR	AH, AH
	PUSH	AX		; STORE THE DIGIT

	; MULTIPLY DI BY 10
	MOV	AX, DI
	MOV	BX, 10
	MUL	BX		; DX:AX = AX * 10

	; IF DX > 0, RESULT EXCEEDED UINT16_T
	CMP	DX, 0
	JA	ERR_BOUNDS_LBL

	POP	BX		; RETRIEVE THE STORED DIGIT
	ADD	AX, BX
	JC	ERR_BOUNDS_LBL	; IF ADD AX, BX CAUSED A UINT16_T OVERFLOW

	MOV	DI, AX		; MOVE CALCULATION BACK TO ACCUMULATOR

	; ENFORCE STRICT BOUNDS
	CMP	IS_NEG, 1
	JNE	NEXT_DIGIT

	CMP	DI, 32669	; PREVENT UNDERFLOW
	JA	ERR_BOUNDS_LBL
	JMP	NEXT_DIGIT

NEXT_DIGIT:
	LOOP	PARSE_LOOP

	; FINALIZE THE REGISTER STATE
	MOV	AX, DI
	CMP	IS_NEG, 1
	JNE	SET_LARGE_FLAG

	NEG	AX		; APPLY THE MINUS SIGN
	JMP	READ_DONE

SET_LARGE_FLAG:
	CMP	AX, 32767
	JBE	READ_DONE
	MOV	IS_LARGE_U, 1	; FLAG THAT MSB=1 ACTUALLY MEANS LARGE POSITIVE

READ_DONE:
	RET

ERR_CHAR_LBL:
	MOV	AH, 09H
	LEA	DX, MSG_ERR_C
	INT	21H
	MOV	AX, 4C01H
	INT	21H

ERR_BOUNDS_LBL:
	MOV	AH, 09H
	LEA	DX, MSG_ERR_O
	INT	21H
	MOV	AX, 4C01H
	INT	21H
READ_N	ENDP

PRINT_N	PROC
	; CHECK IF VALUE SHOULD BE TREATED AS NEGATIVE
	CMP	IS_LARGE_U, 1
	JE	BEGIN_DIV	; IF >32767, POSITIVE

	TEST	AX, AX
	JNS	BEGIN_DIV	; IF SF=0, POSITIVE

	; PRINT MINUS SIGN, MAKE AX POSITIVE FOR DIVISION
	PUSH	AX
	MOV	AH, 02H
	MOV	DL, '-'
	INT	21H
	POP	AX
	NEG	AX

BEGIN_DIV:
	XOR	CX, CX
	MOV	BX, 10

DIV_LOOP:
	XOR	DX, DX
	DIV	BX

	; DIV DONE, TURNING NUM -> ASCII BACK AND STORING IT
	ADD	DL, '0'
	PUSH	DX
	INC	CX

	TEST	AX, AX
	JNZ	DIV_LOOP

PRINT_LOOP:
	POP	DX
	MOV	AH, 02H
	INT	21H
	LOOP	PRINT_LOOP
	
	RET
PRINT_N	ENDP

END	MAIN
